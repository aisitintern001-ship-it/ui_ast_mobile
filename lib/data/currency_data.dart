  /// Get currency code from the display string
  static String? getCurrencyCode(String? displayString) {
    if (displayString == null) return null;
    final match = RegExp(r'\(([A-Z]{3})\)$').firstMatch(displayString);
    return match?.group(1);
  }

  /// Get country name from the display string
  static String? getCountryName(String? displayString) {
    if (displayString == null) return null;
    final match = RegExp(r'^(.+?)\s*\([A-Z]{3}\)$').firstMatch(displayString);
    return match?.group(1)?.trim();
  }
