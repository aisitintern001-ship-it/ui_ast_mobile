  /// Get regions for a given country
  static List<String> getRegions(String? country) {
    if (country == null) return [];
    return regionsByCountry[country] ?? [];
  }

  /// Get provinces for a given region
  static List<String> getProvinces(String? region) {
    if (region == null) return [];
    return provincesByRegion[region] ?? [];
  }

  /// Get cities for a given province
  static List<String> getCities(String? province) {
    if (province == null) return [];
    return citiesByProvince[province] ?? [];
  }

  /// Get country by name
  static Map<String, String>? getCountryByName(String name) {
    try {
      return countries.firstWhere((c) => c['name'] == name);
    } catch (_) {
      return null;
    }
  }

  /// Get country flag by name
  static String getCountryFlag(String countryName) {
    final country = getCountryByName(countryName);
    return country?['flag'] ?? '\u{1F30D}';
  }

  /// Get country code by name
  static String? getCountryCode(String countryName) {
    final country = getCountryByName(countryName);
    return country?['code'];
  }
